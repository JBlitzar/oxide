#[inline]
pub fn blue_noise_slice() -> &'static [u8] {
    &BLUE_NOISE_U8
}

#[inline]
pub fn blue_noise2_slice() -> &'static [u8] {
    &BLUE_NOISE2_U8
}

#[inline]
pub fn blue_noise_at(x: usize, y: usize) -> u8 {
    debug_assert!(x < BLUE_NOISE_WIDTH);
    debug_assert!(y < BLUE_NOISE_HEIGHT);
    BLUE_NOISE_U8[y * BLUE_NOISE_WIDTH + x]
}

#[inline]
pub fn blue_noise2_at(x: usize, y: usize) -> u8 {
    debug_assert!(x < BLUE_NOISE_WIDTH);
    debug_assert!(y < BLUE_NOISE_HEIGHT);
    BLUE_NOISE2_U8[y * BLUE_NOISE_WIDTH + x]
}

#[inline]
pub fn get_pixel_sample(x: usize, y: usize) -> (f64, f64) {
    let tile_x = x / BLUE_NOISE_WIDTH;
    let tile_y = y / BLUE_NOISE_HEIGHT;
    let in_x = x % BLUE_NOISE_WIDTH;
    let in_y = y % BLUE_NOISE_HEIGHT;

    let h0 = ((tile_y as f64 + 1.61803398875 * tile_x as f64) * 43758.5453).sin();
    let h1 = ((tile_y as f64 + 1.3247179572447458 * tile_x as f64) * 24634.6345).sin();
    let j0 = h0.rem_euclid(1.0);
    let j1 = h1.rem_euclid(1.0);

    let a = (blue_noise_at(in_x, in_y) as f64 + 0.5) / 256.0;
    let b = (blue_noise2_at(in_x, in_y) as f64 + 0.5) / 256.0;

    ((a + j0).fract(), (b + j1).fract())
}
